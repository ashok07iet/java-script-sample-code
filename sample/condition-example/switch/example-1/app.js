var day=Number(prompt("please enter day of week"));

switch(day){

    case 1:
      document.write("<p>Sun</p>");
        break;
        case 2:
      document.write("<p>Mon</p>");
        break;
        case 3:
      document.write("<p>Tue</p>");
        break;
        case 4:
      document.write("<p>Wed</p>");
        break;
        case 5:
      document.write("<p>Th</p>");
        break;
        case 6:
      document.write("<p>Fri</p>");
        break;
        case 7:
      document.write("<p>Sat</p>");
        break;
    default:
        document.write("<p>please enter a valid day</p");


}
